/**
 * Takes in an indefinite number of individual numbers and returns their sum
 * @param  {...number} numbers
 * @returns {number} sum of arguments
 */
function sumNumbers() {
  let sum = 0;
  for (number of arguments) {
    sum += number;
  }
  return sum;
}

console.log(sumNumbers(1, 2, 3, 4, 5, 6));
